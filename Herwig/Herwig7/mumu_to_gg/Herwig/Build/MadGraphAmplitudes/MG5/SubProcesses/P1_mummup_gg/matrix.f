      SUBROUTINE MG5_1_SMATRIX(P,ANS)
C     
C     Generated by MadGraph5_aMC@NLO v. 2.2.3, 2015-02-10
C     By the MadGraph5_aMC@NLO Development Team
C     Visit launchpad.net/madgraph5 and amcatnlo.web.cern.ch
C     
C     MadGraph5_aMC@NLO StandAlone Version
C     
C     Returns amplitude squared summed/avg over colors
C     and helicities
C     for the point in phase space P(0:3,NEXTERNAL)
C     
C     Process: mu- mu+ > g g HIG=1 HIW=1 QED=2 QCD=2 @1
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=4)
      INTEGER                 NCOMB
      PARAMETER (             NCOMB=16)
C     
C     ARGUMENTS 
C     
      REAL*8 P(0:3,NEXTERNAL),ANS
C     
C     LOCAL VARIABLES 
C     
      INTEGER NHEL(NEXTERNAL,NCOMB),NTRY
      REAL*8 T
      REAL*8 MG5_1_MATRIX
      INTEGER IHEL,IDEN, I
      INTEGER JC(NEXTERNAL)
      LOGICAL GOODHEL(NCOMB)
      DATA NTRY/0/
      DATA GOODHEL/NCOMB*.FALSE./
      DATA (NHEL(I,   1),I=1,4) / 1,-1,-1,-1/
      DATA (NHEL(I,   2),I=1,4) / 1,-1,-1, 1/
      DATA (NHEL(I,   3),I=1,4) / 1,-1, 1,-1/
      DATA (NHEL(I,   4),I=1,4) / 1,-1, 1, 1/
      DATA (NHEL(I,   5),I=1,4) / 1, 1,-1,-1/
      DATA (NHEL(I,   6),I=1,4) / 1, 1,-1, 1/
      DATA (NHEL(I,   7),I=1,4) / 1, 1, 1,-1/
      DATA (NHEL(I,   8),I=1,4) / 1, 1, 1, 1/
      DATA (NHEL(I,   9),I=1,4) /-1,-1,-1,-1/
      DATA (NHEL(I,  10),I=1,4) /-1,-1,-1, 1/
      DATA (NHEL(I,  11),I=1,4) /-1,-1, 1,-1/
      DATA (NHEL(I,  12),I=1,4) /-1,-1, 1, 1/
      DATA (NHEL(I,  13),I=1,4) /-1, 1,-1,-1/
      DATA (NHEL(I,  14),I=1,4) /-1, 1,-1, 1/
      DATA (NHEL(I,  15),I=1,4) /-1, 1, 1,-1/
      DATA (NHEL(I,  16),I=1,4) /-1, 1, 1, 1/
      DATA IDEN/ 8/
C     ----------
C     BEGIN CODE
C     ----------
      NTRY=NTRY+1
      DO IHEL=1,NEXTERNAL
        JC(IHEL) = +1
      ENDDO
      ANS = 0D0
      DO IHEL=1,NCOMB
        IF (GOODHEL(IHEL) .OR. NTRY .LT. 20) THEN
          T=MG5_1_MATRIX(P ,NHEL(1,IHEL),JC(1))
          ANS=ANS+T
          IF (T .NE. 0D0 .AND. .NOT.    GOODHEL(IHEL)) THEN
            GOODHEL(IHEL)=.TRUE.
          ENDIF
        ENDIF
      ENDDO
      ANS=ANS/DBLE(IDEN)
      END


      REAL*8 FUNCTION MG5_1_MATRIX(P,NHEL,IC)
C     
C     Generated by MadGraph5_aMC@NLO v. 2.2.3, 2015-02-10
C     By the MadGraph5_aMC@NLO Development Team
C     Visit launchpad.net/madgraph5 and amcatnlo.web.cern.ch
C     
C     Returns amplitude squared summed/avg over colors
C     for the point with external lines W(0:6,NEXTERNAL)
C     
C     Process: mu- mu+ > g g HIG=1 HIW=1 QED=2 QCD=2 @1
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NGRAPHS
      PARAMETER (NGRAPHS=1)
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=4)
      INTEGER    NWAVEFUNCS, NCOLOR
      PARAMETER (NWAVEFUNCS=5, NCOLOR=1)
      REAL*8     ZERO
      PARAMETER (ZERO=0D0)
      COMPLEX*16 IMAG1
      PARAMETER (IMAG1=(0D0,1D0))
C     
C     ARGUMENTS 
C     
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL), IC(NEXTERNAL)
C     
C     LOCAL VARIABLES 
C     
      INTEGER I,J
      COMPLEX*16 ZTEMP
      REAL*8 DENOM(NCOLOR), CF(NCOLOR,NCOLOR)
      COMPLEX*16 AMP(NGRAPHS), JAMP(NCOLOR), LNJAMP(NCOLOR)
      COMPLEX*16 W(18,NWAVEFUNCS)
      COMPLEX*16 DUM0,DUM1
      DATA DUM0, DUM1/(0D0, 0D0), (1D0, 0D0)/
C     
C     GLOBAL VARIABLES
C     
      INCLUDE 'coupl.inc'

C     
C     COLOR DATA
C     
      DATA DENOM(1)/1/
      DATA (CF(I,  1),I=  1,  1) /    2/
C     1 Tr(3,4)
C     ----------
C     BEGIN CODE
C     ----------
      CALL IXXXXX(P(0,1),MDL_MM,NHEL(1),+1*IC(1),W(1,1))
      CALL OXXXXX(P(0,2),MDL_MM,NHEL(2),-1*IC(2),W(1,2))
      CALL VXXXXX(P(0,3),ZERO,NHEL(3),+1*IC(3),W(1,3))
      CALL VXXXXX(P(0,4),ZERO,NHEL(4),+1*IC(4),W(1,4))
      CALL FFS2_3(W(1,1),W(1,2),GC_86,MDL_MH,MDL_WH,W(1,5))
C     Amplitude(s) for diagram number 1
      CALL VVS3_0(W(1,3),W(1,4),W(1,5),GC_13,AMP(1))
      JAMP(1)=+2D0*(+AMP(1))
      LNJAMP(1)=+2D0*(+AMP(1))

      MG5_1_MATRIX = 0.D0
      DO I = 1, NCOLOR
        ZTEMP = (0.D0,0.D0)
        DO J = 1, NCOLOR
          ZTEMP = ZTEMP + CF(J,I)*JAMP(J)
        ENDDO
        MG5_1_MATRIX = MG5_1_MATRIX+ZTEMP*DCONJG(JAMP(I))/DENOM(I)
      ENDDO

      END










