      SUBROUTINE MG5_1_GET_NCOLOR(IN1, IN2, OUT)

      INTEGER IN1, IN2, OUT

      IF (IN1.EQ.0.AND.IN2.EQ.1)THEN
        OUT = 4


      ELSEIF (IN1.EQ.0.AND.IN2.EQ.0)THEN
        OUT = 3


      ELSEIF (IN1.EQ.0.AND.IN2.EQ.2)THEN
        OUT = 0

      ELSE
        OUT = - 1
      ENDIF
      END

